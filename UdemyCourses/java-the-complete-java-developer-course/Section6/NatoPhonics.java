public class NatoPhonics {

    public static void main(String[] args) {

        char letter = 'C';

        switch(letter) {
            case 'A':
                System.out.println("Alpha");
                break;
            case 'B':
                System.out.println("Bravo");
                break;
            case 'C':
                System.out.println("Charlie");
                break;
            default:
                System.out.println("Not NATO");
                break;
        }
    }
}